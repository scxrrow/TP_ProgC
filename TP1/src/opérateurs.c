#include <stdio.h>
#include <math.h>

int main() {
    int a = 16;
    int b = 3;

    // Addition
    printf("a + b = %d\n", a + b);

    // soustraction
    printf("a - b = %d\n", a - b);

    // Multiplication
    printf("a * b = %d\n", a * b);

    // Division
    printf("a / b = %d\n", a / b);

    // Modulo
    printf("a %% b = %d\n", a % b);

    // comparaison égalité
    printf("a == b: %d\n", a == b);

    // Comparaison plus grand que
    printf("a > b: %d\n", a > b);
}
